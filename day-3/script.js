const arrMap = map.split('\n');

const howManyTrees = (map, right, down) => {
    // repeating the map as manu times as needed
    const googleMaps = map.map((el) => {
        const lineBreak = Math.floor(map[0].length / right);
        const nRepeat = Math.ceil(map.length / lineBreak);
        return el.repeat(nRepeat);
    });

    let treeCounter = 0;
    let j = right;
    let i = down;
    while (i < googleMaps.length) {
        const row = googleMaps[i];
        if (row.charAt(j) === '#') treeCounter++;
        j += right;
        i += down;
    }

    // return `${treeCounter} trees found`;
    return treeCounter;
};

console.log(howManyTrees(arrMap, 3, 1));

